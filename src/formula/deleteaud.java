    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        String auo = tx_ticketid.getText();
        if(auo.equals("")){
            JOptionPane.showMessageDialog(this, "You search for nothing");
        }else{
        try {
            sql = "SELECT * FROM `tbl_penonton` WHERE `id_ticket` = ?";
            ps = conn.prepareStatement(sql);
            ps.setString(1, auo);
            rs = ps.executeQuery();
            if(rs==null){
                JOptionPane.showMessageDialog(this, "no data");
            }
            while(rs.next()){
                colorcombow.setSelectedItem(rs.getString(2));
                tx_firstname.setText(rs.getString(3));
                tx_lastname.setText(rs.getString(4));
                ag.setSelectedItem(rs.getString(5));
                tx_city.setText(rs.getString(6));
                tx_nophone.setText(rs.getString(7));
                JOptionPane.showMessageDialog(this, "Data found");
            }
            
        } catch (Exception e) {
            System.out.println("Error "+e.getMessage());
            JOptionPane.showMessageDialog(this, "No Data");
        }
        }
         btn_delete.setEnabled(true);
datatable();
    }//GEN-LAST:event_btn_searchActionPerformed

