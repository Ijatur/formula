    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
        if(tx_city.getText().isEmpty() || tx_firstname.getText().isEmpty() || 
                tx_lastname.getText().isEmpty() || tx_nophone.getText().isEmpty()
                || tx_ticketid.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill in all the blanks");
        }else
        try{
            sql = "UPDATE `tbl_penonton` SET `ticket_color`=?,`firstname`=?,`lastname`=?,`age`=?,"
                    + "`city`=?,`no_phone`=?,`id_ticket`=? WHERE id_ticket=?";
            ps = conn.prepareStatement(sql);
            ps.setString(1, colorcombow.getSelectedItem().toString());
            ps.setString(2, tx_firstname.getText());
            ps.setString(3, tx_lastname.getText());
            ps.setString(4, ag.getSelectedItem().toString());
            ps.setString(5, tx_city.getText());
            ps.setString(6, tx_nophone.getText());
            ps.setString(7, tx_ticketid.getText());
            ps.setString(8, tx_ticketid.getText());
            int rt = ps.executeUpdate();
            if(rt!=0){
                JOptionPane.showMessageDialog(this, "data successfully updated");
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "data gagal");
            System.out.println("Error"+e.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btnUpActionPerformed

